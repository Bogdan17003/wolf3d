#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int		len1;
	size_t	i;

	len1 = ft_strlen(s1);
	i = 0;
	while (s2[i] && i < n)
	{
		s1[len1] = s2[i];
		len1++;
		i++;
	}
	s1[len1] = '\0';
	return (s1);
}
