#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int len1;
	int i;

	len1 = ft_strlen(s1);
	i = 0;
	while (s2[i])
	{
		s1[len1] = s2[i];
		len1++;
		i++;
	}
	s1[len1] = '\0';
	return (s1);
}
